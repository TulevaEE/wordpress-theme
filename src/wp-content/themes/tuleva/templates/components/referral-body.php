                        1. Tegutse kohe. Mida varem teed hea valiku, seda suurem on mõju su pensionile.
                    </a>
                    <div id="answer-1" class="collapse mb-3">
                        <p>
                            “Liitintress on kaheksas maailmaime. Kes aru saab, teenib seda. Kes ei saa, maksab seda.” Albert Einstein
                        </p>

                        <p>
                            Üks paradoks, mis kipub töötama sinu “tuleviku-mina” kahjuks:
                            <ul>
                                <li>mida noorem sa oled, seda raskem on sul pensionile mõtlemisest
                            innustuda, aga</li>
                                <li>mida nooremalt sa head valikud teed, seda vähem pead sama suure pensioni kogumiseks
                            ohverdama.</li>
                            </ul>
                        </p>

                        <p>Kui teed 20-aastaselt valiku, mis suurendab sinu pensionifondi tulu keskmiselt 1% võrra aastas, saad
                            26% võrra suurema pensioni. Tehes sama 35-aastaselt, suurendad oma pensioni viiendiku võrra.
                            50-aastaselt tehtud valik parandab pensionit 10% võrra. </p>

                        <p>Seega: anna kohe täna vähemalt sellele rahale, mis sinu palgast iga kuu pensionifondi läheb, parimad
                            võimalused kasvuks. Järgmised neli soovitust on siin abiks. </p>
