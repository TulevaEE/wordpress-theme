                        4. Kulud on tähtsamad kui sa arvad. Ära maksa tasudeks rohkem kui pool protsenti.
                    </a>
                    <div id="answer-4" class="collapse mb-3">
                        <p>“On üks usaldusväärne soovitus, mille järgimine sinu investeerimistulu parandab: hoia investeeringu
                            kulud võimalikult madalal.” Burton G. Malkiel </p>

                        <p>Uurimused näitavad, et investeerimisfondi kulud on kõige töökindlam pikaajaliste tulemuste ennustaja:
                            mida madalamad kulud, seda parem tulemus - ja vastupidi. Kõrged tasud on pikaajalise säästja
                            suur vaenlane. </p>

                        <p>Tasude mõju on hiiliv - algul tillukesena mõjuvad numbrid söövad aastate jooksul sinu pensionivara
                            samamoodi väiksemaks nagu näiteks eluasemelaenu intressid kasvatavad seda summat, mida sa pangale
                            tagasi maksma pead. </p>

                        <p>Võtame näiteks Sanderi, kes on täna 25-aastane ja teenib Eesti keskmist palka. Oletame, et ta palk
                            tõuseb aastas 3% ja ta on valinud agressiivse strateegiaga pensionifondi, mille keskmine tootlus
                            aastas enne tasusid on Sanderi tööelu vältel 5% aastas. </p>

                        <p>Kui Sanderi pensionifondi jooksvad tasud (valitsemistasu + muud investeeringu kulud) on 0,5% aastas
                            nagu Eesti soodsaima tasuga pensionifondidel, koguneb tema pensionikontole 65ndaks eluaastaks
                            160 tuhat eurot. Kui aga jooksvad tasud on 1,6% aastas nagu Eesti kõrgeima tasuga pensionifondidel,
                            koguneb talle ainult 127 tuhat eurot. Tasude tõttu kaotab Sander 33 tuhat eurot! </p>

                        <p>Pole kokkusattumus, et Eesti pankade pensionifondid on aastaid maailmas silma paistnud üheaegselt
                            kõrgete tasude ja kehvade tulemuste poolest. Nüüd on sinu valikus ka kaasaegsed, madala kuluga
                            fondid. Ära vaata ainult valitsemistasu. Küsi oma fondivalitsejalt pensionifondi jooksvate tasude
                            suurust.
                        </p>
