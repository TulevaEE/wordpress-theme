                        3. Ära püüa turgu ajastada. Regulaarne investeerimine on parim riskide maandaja.
                    </a>
                    <div id="answer-3" class="collapse mb-3">
                        <p>“Investorid on kaotanud palju rohkem raha turu languseks valmistudes või seda ette näha püüdes kui
                            turu langustes endis.” Peter Lynch</p>

                        <p>Pensioni teise sambasse kogudes teed juba ühe tähtsa asja õigesti: sa investeerid regulaarselt väikeste
                            summade kaupa - 6% palgast iga kuu. Sinu kasuks töötab automaatselt pikaajalise investori parim
                            riskide maandamise tööriist: dollar cost averaging ehk ostude ajas hajutamine.</p>

                        <p>Sinu elu vältel jõuavad väärtpaberiturud läbi teha mitu vahvat tõusu ja mitu valusat kukkumist. Tänu
                            ostude ajas hajutamisele ei pea sa pead murdma, millal järgmine buum või kriis tuleb. See tagab,
                            et sinu keskmine ostuhind alati madalam kui kõige kõrgemalt tasemelt tehtud ostud – ja pikas
                            plaanis on sul head eeldused selleks, et sinu portfell kenasti kasvaks. Passiivselt juhitud pensionifondis
                            ehk indeksifondis on ostude ajas hajutamine automaatne. Sellise fondi ei jäta su raha kunagi
                            kartlikult pangaarvele seisma ega saa ka ühelgi hetkel liiga uljasse hoogu sattuda.</p>
                    </div>
                </div>
