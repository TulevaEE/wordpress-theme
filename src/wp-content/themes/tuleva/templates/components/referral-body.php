                    <a class="btn btn-link principle__item" data-toggle="collapse" href="#answer-2">
                        2. Vali agressiivne fond. Pea meeles, et riskivaba tulu pole olemas.
                    </a>
                    <div id="answer-2" class="collapse mb-3">
                        <p>“Aktsiaturg on vahend raha liigutamiseks kannatamatute käest kannatlike kätte.” Warren Buffett. </p>

                        <p>Vähesed meist on piisavalt rikkad, et koguda pensioniks piisavalt raha ilma riski võtmata. Muidugi
                            võiks hoida sääste lihtsalt madratsi sees, aga elukalliduse kasv sööb aastate jooksul suure osa
                            raha väärtusest ära. Kui pensionifondi juht hoiab suurt osa rahast nullilähedase tootlusega võlakirjades
                            või pangaarvel, on veel halvem: inflatsioonile lisandub iga-aastase valitsemistasu negatiivne
                            mõju.
                        </p>

                        <p>Eestis on kokku nelja sildiga pensionifonde: “konservatiivsed”, “tasakaalustatud”, “progressiivsed”
                            ja “agressiivsed”. Need sildid tähistavad seda, kui suure osa varast võib pensionifond aktsiatesse
                            investeerida. Kuidas valida? </p>


                        <p>Tulevas oleme veendunud, et kui sul on pensionini rohkem kui kümme aastat, on tark investeerida aktsiatesse
                            nii suure osa oma pensionivarast kui seadus vähegi lubab. Sama soovitab oma pensionikogujatele
                            näiteks Rootsi riik - tulemused on seni väga head. Miks? </p>

                        <p>Aktsiate hinnad kõiguvad majandustsüklites rohkem kui võlakirjade hinnad. Ja samas: kui vaadata ükskõik
                            millist kümnendit turgude ajaloos, on aktsiad andnud alati parema tootluse kui võlakirjad. Minevik
                            pole tuleviku garantiiks. Aga investeerides pikaks ajaks - näiteks pensioniks kogudes - saad
                            väga suure tõenäosusega tõenäoliselt järjekindlalt aktsiaid eelistades lõpuks oluliselt parema
                            tulemuse. </p>

                        <p>Seega: vali agressiivse strateegiaga fond, mis investeerib kuni kolmveerandi varast aktsiatesse.
                            Lepi sellega, et aktsiate hindade kõikumised on järsemad kui võlakirjadel. Need kõikumised ei
                            ole sinu vaenlased. Muretsema peaksid pigem sellepärast, kui sinu pensionifondi juht kulutab
                            oma aega ja sinu raha, et lühiajalisi kõikumisi siluda. Pikas plaanis teeb see sinu tulemuse
                            halvemaks. </p>
